const NORMALIZED_SIZE = 100;

const transformPath = (
    d: string,
    transform: (x: number, y: number) => [number, number]
): string => {
    return d.replace(
        /([MLC])\s*([^MLCZ]+)/gi,
        (_match, command: string, coordsStr: string) => {
            const numbers = coordsStr.trim().split(/[\s,]+/).map(Number);
            const transformed: number[] = [];
            for (let i = 0; i < numbers.length; i += 2) {
                const [x, y] = transform(numbers[i], numbers[i + 1]);
                transformed.push(x, y);
            }
            const formatted: string[] = [];
            for (let i = 0; i < transformed.length; i += 2) {
                formatted.push(
                    `${transformed[i].toFixed(3)},${transformed[i + 1].toFixed(3)}`
                );
            }
            return `${command} ${formatted.join(' ')} `;
        }
    ).replace(/\s*Z\s*$/i, ' Z');
};

const getBoundingBox = (
    d: string
): { minX: number; minY: number; maxX: number; maxY: number } => {
    let minX = Infinity;
    let minY = Infinity;
    let maxX = -Infinity;
    let maxY = -Infinity;
    d.replace(/([MLC])\s*([^MLCZ]+)/gi, (_match, _cmd, coordsStr: string) => {
        const numbers = coordsStr.trim().split(/[\s,]+/).map(Number);
        for (let i = 0; i < numbers.length; i += 2) {
            const x = numbers[i];
            const y = numbers[i + 1];
            if (x < minX) minX = x;
            if (y < minY) minY = y;
            if (x > maxX) maxX = x;
            if (y > maxY) maxY = y;
        }
        return '';
    });
    return { minX, minY, maxX, maxY };
};

const normalizePath = (d: string): string => {
    const { minX, minY, maxX, maxY } = getBoundingBox(d);
    const width = maxX - minX;
    const height = maxY - minY;
    const scale = NORMALIZED_SIZE / Math.max(width, height);
    const offsetX = (NORMALIZED_SIZE - width * scale) / 2;
    const offsetY = (NORMALIZED_SIZE - height * scale) / 2;
    return transformPath(d, (x, y) => [
        (x - minX) * scale + offsetX,
        (y - minY) * scale + offsetY,
    ]);
};

